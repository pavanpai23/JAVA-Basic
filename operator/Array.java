package operator;


public class Array {
    public static void main(String[] args) {

        //operators
        int a=4;
        int b=6;
        int sum=a+b;
        System.out.println(sum);

        int diff=a-b;
        System.out.println(diff);

        int product=a*b;
        System.out.println(product);

        int div=a/b;
        System.out.println(div);

        //modulo
        int modulo=a%b;
        System.out.println(modulo);
    }
}